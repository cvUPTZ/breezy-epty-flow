
import { useState, useEffect, useRef, useCallback } from 'react';
import { supabase } from '@/integrations/supabase/client';
import { toast } from 'sonner';

interface VoiceCollaborationOptions {
  matchId: string;
  userId: string;
  onUserJoined?: (userId: string) => void;
  onUserLeft?: (userId: string) => void;
}

interface TrackerVoiceStatus {
  userId: string;
  isMuted: boolean;
  isSpeaking: boolean;
  isConnected: boolean;
  username?: string;
  role?: 'main' | 'specialized' | 'admin';
  audioLevel?: number;
}

interface VoiceRoom {
  id: string;
  name: string;
  maxParticipants: number;
  currentParticipants: number;
  isPrivate: boolean;
}

export const useVoiceCollaboration = ({
  matchId,
  userId,
  onUserJoined,
  onUserLeft
}: VoiceCollaborationOptions) => {
  console.log('üé§ useVoiceCollaboration: INITIALIZING', { matchId, userId });
  
  const [isVoiceEnabled, setIsVoiceEnabled] = useState(false);
  const [isMuted, setIsMuted] = useState(true);
  const [isConnecting, setIsConnecting] = useState(false);
  const [connectedTrackers, setConnectedTrackers] = useState<TrackerVoiceStatus[]>([]);
  const [audioLevel, setAudioLevel] = useState(0);
  const [currentRoom, setCurrentRoom] = useState<VoiceRoom | null>(null);
  const [availableRooms, setAvailableRooms] = useState<VoiceRoom[]>([]);
  const [isRoomAdmin, setIsRoomAdmin] = useState(false);
  const [debugInfo, setDebugInfo] = useState<string[]>([]);

  const localStreamRef = useRef<MediaStream | null>(null);
  const peerConnectionsRef = useRef<Map<string, RTCPeerConnection>>(new Map());
  const channelRef = useRef<any>(null);
  const audioContextRef = useRef<AudioContext | null>(null);
  const analyserRef = useRef<AnalyserNode | null>(null);
  const gainNodeRef = useRef<GainNode | null>(null);
  const remoteAudiosRef = useRef<Map<string, HTMLAudioElement>>(new Map());

  // Add debug info helper
  const addDebugInfo = useCallback((message: string) => {
    const timestamp = new Date().toISOString().slice(11, 23);
    const debugMessage = `[${timestamp}] ${message}`;
    console.log('üîç VOICE DEBUG:', debugMessage);
    setDebugInfo(prev => [...prev.slice(-9), debugMessage]); // Keep last 10 messages
  }, []);

  // Comprehensive browser capability check
  const checkBrowserCapabilities = useCallback(() => {
    addDebugInfo('üîç Checking browser capabilities...');
    
    // Check getUserMedia support
    if (!navigator.mediaDevices || !navigator.mediaDevices.getUserMedia) {
      addDebugInfo('‚ùå getUserMedia not supported');
      return false;
    }
    addDebugInfo('‚úÖ getUserMedia supported');

    // Check WebRTC support
    if (!window.RTCPeerConnection) {
      addDebugInfo('‚ùå WebRTC not supported');
      return false;
    }
    addDebugInfo('‚úÖ WebRTC supported');

    // Check AudioContext support
    const AudioContext = window.AudioContext || (window as any).webkitAudioContext;
    if (!AudioContext) {
      addDebugInfo('‚ùå AudioContext not supported');
      return false;
    }
    addDebugInfo('‚úÖ AudioContext supported');

    // Check if HTTPS or localhost
    const isSecure = location.protocol === 'https:' || location.hostname === 'localhost';
    addDebugInfo(`üîí Secure context: ${isSecure ? 'YES' : 'NO'}`);
    
    return true;
  }, [addDebugInfo]);

  // Enhanced audio constraints with debugging
  const getAudioConstraints = useCallback(() => {
    addDebugInfo('üéõÔ∏è Setting up audio constraints');
    return {
      audio: {
        echoCancellation: true,
        noiseSuppression: true,
        autoGainControl: true,
        sampleRate: 48000,
        channelCount: 1,
        latency: 0.01,
        googEchoCancellation: true,
        googNoiseSuppression: true,
        googAutoGainControl: true,
        googHighpassFilter: true,
        googTypingNoiseDetection: true,
        googAudioMirroring: false,
        googBeamforming: true,
        googArrayGeometry: true
      }
    };
  }, [addDebugInfo]);

  // Setup audio analysis
  const setupAudioAnalysis = useCallback((stream: MediaStream) => {
    addDebugInfo('üéµ Setting up audio analysis');
    try {
      const AudioContext = window.AudioContext || (window as any).webkitAudioContext;
      audioContextRef.current = new AudioContext();
      
      const source = audioContextRef.current.createMediaStreamSource(stream);
      analyserRef.current = audioContextRef.current.createAnalyser();
      gainNodeRef.current = audioContextRef.current.createGain();
      
      analyserRef.current.fftSize = 256;
      source.connect(analyserRef.current);
      analyserRef.current.connect(gainNodeRef.current);
      
      addDebugInfo('‚úÖ Audio analysis setup complete');
      
      // Start monitoring audio levels
      const monitorAudioLevel = () => {
        if (!analyserRef.current) return;
        
        const bufferLength = analyserRef.current.frequencyBinCount;
        const dataArray = new Uint8Array(bufferLength);
        analyserRef.current.getByteFrequencyData(dataArray);
        
        const average = dataArray.reduce((sum, value) => sum + value, 0) / bufferLength;
        const normalizedLevel = average / 255;
        setAudioLevel(normalizedLevel);
        
        if (normalizedLevel > 0.1) {
          addDebugInfo(`üîä Audio detected: ${(normalizedLevel * 100).toFixed(1)}%`);
        }
      };
      
      const intervalId = setInterval(monitorAudioLevel, 100);
      return () => clearInterval(intervalId);
      
    } catch (error) {
      addDebugInfo(`‚ùå Audio analysis setup failed: ${error}`);
      console.error('Audio analysis setup error:', error);
    }
  }, [addDebugInfo]);

  // Initialize rooms with debugging
  const initializeVoiceRooms = useCallback(() => {
    addDebugInfo('üè† Initializing voice rooms');
    const rooms: VoiceRoom[] = [
      {
        id: `${matchId}_main`,
        name: 'Main Communication',
        maxParticipants: 20,
        currentParticipants: 0,
        isPrivate: false
      },
      {
        id: `${matchId}_coordinators`,
        name: 'Match Coordinators',
        maxParticipants: 8,
        currentParticipants: 0,
        isPrivate: true
      },
      {
        id: `${matchId}_team_a`,
        name: 'Team A Trackers',
        maxParticipants: 25,
        currentParticipants: 0,
        isPrivate: false
      },
      {
        id: `${matchId}_team_b`,
        name: 'Team B Trackers',
        maxParticipants: 25,
        currentParticipants: 0,
        isPrivate: false
      },
      {
        id: `${matchId}_events`,
        name: 'Event Specialists',
        maxParticipants: 15,
        currentParticipants: 0,
        isPrivate: false
      },
      {
        id: `${matchId}_technical`,
        name: 'Technical Support',
        maxParticipants: 10,
        currentParticipants: 0,
        isPrivate: false
      }
    ];
    setAvailableRooms(rooms);
    addDebugInfo(`‚úÖ ${rooms.length} voice rooms initialized`);
  }, [matchId, addDebugInfo]);

  // Enhanced join voice room with comprehensive debugging
  const joinVoiceRoom = useCallback(async (room: VoiceRoom) => {
    addDebugInfo(`üö™ Attempting to join room: ${room.name}`);
    
    if (!checkBrowserCapabilities()) {
      addDebugInfo('‚ùå Browser capabilities check failed');
      toast.error('Your browser does not support voice chat');
      return;
    }
    
    if (isVoiceEnabled || room.currentParticipants >= room.maxParticipants) {
      addDebugInfo(`‚ùå Cannot join room - already enabled: ${isVoiceEnabled}, room full: ${room.currentParticipants}/${room.maxParticipants}`);
      return;
    }

    setIsConnecting(true);
    addDebugInfo('üîÑ Setting connecting state');
    
    try {
      addDebugInfo('üé§ Requesting microphone access...');
      const constraints = getAudioConstraints();
      addDebugInfo(`üìã Audio constraints: ${JSON.stringify(constraints.audio, null, 2)}`);
      
      const stream = await navigator.mediaDevices.getUserMedia(constraints);
      addDebugInfo(`‚úÖ Microphone access granted - tracks: ${stream.getTracks().length}`);
      
      // Log detailed stream info
      stream.getTracks().forEach((track, index) => {
        addDebugInfo(`üì° Track ${index}: ${track.kind}, enabled: ${track.enabled}, muted: ${track.muted}, state: ${track.readyState}`);
        addDebugInfo(`üéõÔ∏è Track settings: ${JSON.stringify(track.getSettings(), null, 2)}`);
      });
      
      localStreamRef.current = stream;
      setCurrentRoom(room);
      addDebugInfo(`üè† Current room set: ${room.name}`);

      // Setup audio analysis
      const cleanupAudioAnalysis = setupAudioAnalysis(stream);
      
      // Setup Supabase channel
      addDebugInfo(`üì° Setting up Supabase channel: voice_${room.id}`);
      channelRef.current = supabase.channel(`voice_${room.id}`);
      
      channelRef.current.subscribe(async (status: string) => {
        addDebugInfo(`üì° Channel subscription status: ${status}`);
        if (status === 'SUBSCRIBED') {
          addDebugInfo('‚úÖ Successfully subscribed to voice channel');
          setIsVoiceEnabled(true);
          toast.success(`Joined ${room.name} - You are muted by default`);
          
          // Test audio immediately
          addDebugInfo('üß™ Testing audio capabilities...');
          if (localStreamRef.current) {
            const audioTrack = localStreamRef.current.getAudioTracks()[0];
            if (audioTrack) {
              addDebugInfo(`üéµ Audio track active: ${audioTrack.enabled}, muted: ${audioTrack.muted}`);
              // Temporarily unmute to test
              audioTrack.enabled = true;
              setTimeout(() => {
                addDebugInfo('üîá Re-muting audio track for default muted state');
                audioTrack.enabled = false;
              }, 1000);
            } else {
              addDebugInfo('‚ùå No audio track found in stream');
            }
          }
        } else if (status === 'CHANNEL_ERROR') {
          addDebugInfo('‚ùå Channel subscription error');
          toast.error('Failed to connect to voice channel');
        } else if (status === 'CLOSED') {
          addDebugInfo('üîí Channel closed');
        }
      });

      // Store cleanup function
      if (cleanupAudioAnalysis) {
        const originalCleanup = cleanupAudioAnalysis;
        (channelRef.current as any).audioCleanup = originalCleanup;
      }

    } catch (error: any) {
      addDebugInfo(`‚ùå Failed to join voice room: ${error.message}`);
      console.error('Voice room join error:', error);
      
      // Detailed error analysis
      if (error.name === 'NotAllowedError') {
        addDebugInfo('üö´ Microphone access denied by user');
        toast.error('Microphone access denied. Please allow microphone access and try again.');
      } else if (error.name === 'NotFoundError') {
        addDebugInfo('üé§ No microphone found');
        toast.error('No microphone found. Please connect a microphone and try again.');
      } else if (error.name === 'NotSupportedError') {
        addDebugInfo('üö´ Browser does not support audio capture');
        toast.error('Your browser does not support audio capture.');
      } else {
        toast.error('Failed to access microphone: ' + error.message);
      }
    } finally {
      setIsConnecting(false);
      addDebugInfo('üîÑ Cleared connecting state');
    }
  }, [isVoiceEnabled, checkBrowserCapabilities, getAudioConstraints, setupAudioAnalysis, addDebugInfo]);

  // Enhanced leave voice room
  const leaveVoiceRoom = useCallback(() => {
    addDebugInfo('üö™ Leaving voice room');
    
    if (!isVoiceEnabled || !currentRoom) {
      addDebugInfo('‚ùå No room to leave');
      return;
    }

    // Stop local stream
    if (localStreamRef.current) {
      addDebugInfo('üõë Stopping local stream');
      localStreamRef.current.getTracks().forEach(track => {
        addDebugInfo(`üõë Stopping track: ${track.kind}`);
        track.stop();
      });
      localStreamRef.current = null;
    }

    // Cleanup audio analysis
    if ((channelRef.current as any)?.audioCleanup) {
      addDebugInfo('üßπ Cleaning up audio analysis');
      (channelRef.current as any).audioCleanup();
    }

    // Stop audio context
    if (audioContextRef.current) {
      addDebugInfo('üîä Closing audio context');
      audioContextRef.current.close();
      audioContextRef.current = null;
    }

    // Unsubscribe from channel
    if (channelRef.current) {
      addDebugInfo('üì° Unsubscribing from channel');
      supabase.removeChannel(channelRef.current);
      channelRef.current = null;
    }

    setIsVoiceEnabled(false);
    setConnectedTrackers([]);
    setAudioLevel(0);
    setIsMuted(true);
    const roomName = currentRoom.name;
    setCurrentRoom(null);
    setIsRoomAdmin(false);
    addDebugInfo(`‚úÖ Left voice room: ${roomName}`);
    toast.info(`Left ${roomName}`);
  }, [isVoiceEnabled, currentRoom, addDebugInfo]);

  // Enhanced mute toggle with debugging
  const toggleMute = useCallback(() => {
    addDebugInfo(`üîá Toggling mute - currently: ${isMuted ? 'MUTED' : 'UNMUTED'}`);
    
    if (localStreamRef.current) {
      const audioTrack = localStreamRef.current.getAudioTracks()[0];
      if (audioTrack) {
        audioTrack.enabled = isMuted;
        setIsMuted(!isMuted);
        addDebugInfo(`üéµ Audio track enabled: ${audioTrack.enabled}, new muted state: ${!isMuted}`);
        toast.info(isMuted ? 'Microphone unmuted' : 'Microphone muted');
      } else {
        addDebugInfo('‚ùå No audio track found for mute toggle');
        toast.error('No audio track available');
      }
    } else {
      addDebugInfo('‚ùå No local stream available for mute toggle');
      toast.error('No audio stream available');
    }
  }, [isMuted, addDebugInfo]);

  // Legacy compatibility functions
  const startVoiceCollaboration = useCallback(async () => {
    addDebugInfo('üîÑ Starting voice collaboration (legacy)');
    const mainRoom = availableRooms.find(room => room.id.includes('_main'));
    if (mainRoom) {
      await joinVoiceRoom(mainRoom);
    } else {
      addDebugInfo('‚ùå No main room found for legacy start');
    }
  }, [availableRooms, joinVoiceRoom, addDebugInfo]);

  const stopVoiceCollaboration = useCallback(() => {
    addDebugInfo('üõë Stopping voice collaboration (legacy)');
    leaveVoiceRoom();
  }, [leaveVoiceRoom, addDebugInfo]);

  // Initialize rooms on mount
  useEffect(() => {
    addDebugInfo('üöÄ Component mounted, initializing');
    initializeVoiceRooms();
  }, [initializeVoiceRooms, addDebugInfo]);

  // Cleanup on unmount
  useEffect(() => {
    return () => {
      addDebugInfo('üßπ Component unmounting, cleaning up');
      if (isVoiceEnabled) {
        leaveVoiceRoom();
      }
    };
  }, [isVoiceEnabled, leaveVoiceRoom, addDebugInfo]);

  // Log current state periodically for debugging
  useEffect(() => {
    const interval = setInterval(() => {
      if (isVoiceEnabled || isConnecting) {
        addDebugInfo(`üìä State: enabled=${isVoiceEnabled}, muted=${isMuted}, connecting=${isConnecting}, room=${currentRoom?.name}, audioLevel=${audioLevel.toFixed(3)}`);
      }
    }, 5000);
    
    return () => clearInterval(interval);
  }, [isVoiceEnabled, isMuted, isConnecting, currentRoom, audioLevel, addDebugInfo]);

  return {
    // Legacy compatibility
    isVoiceEnabled,
    isMuted,
    isConnecting,
    connectedTrackers,
    audioLevel,
    startVoiceCollaboration,
    stopVoiceCollaboration,
    toggleMute,
    
    // New room-based features
    availableRooms,
    currentRoom,
    isRoomAdmin,
    joinVoiceRoom,
    leaveVoiceRoom,
    
    // Debug information
    debugInfo
  };
};
